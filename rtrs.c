void RTRS_free(struct RTRS_CTX *ctx)
{
	BN_clear_free(ctx->q);
	BN_clear_free(ctx->g);
	BN_CTX_free(ctx->bnctx);
	EC_GROUP_clear_free(ctx->curve);
	free(ctx);
	ctx = 0;
}
